package org.eclipse.che.examples;

public class Initialise
{
    public static void main(String[] args)
    {
        int n = 10;
        float m = 20.0f;
        String name = "vaishnavi";
        double p = 10.5;
        char ch = 'o';

        System.out.println("Value of Integer:"+n);
        System.out.println("Value of Float:"+m);
        System.out.println("Value of String:"+name);
        System.out.println("Value of Double:"+p);
        System.out.println("Value of Character:"+ch);





    }



}